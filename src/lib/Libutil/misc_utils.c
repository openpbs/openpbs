/**
 * @brief
 * 	Given a file represented by 'filepath', return its crc value.
 *
 * @param[in]	filepath	- file being crc-ed.
 *
 * @return u_long
 * @retval	> 0	- crc (checksum) value of the file.
 * @retval	0	- if file is non-existent, or file is empty, or if an
 * 			  error encountered while opening or reading the
 * 			  file.
 */
unsigned long
crc_file(char *filepath)
{
	int     fd;
	struct stat sb;
	static u_char *buf = NULL;
	static	int buf_sz = 0;
	u_char	*tmp_str = NULL;
	int	nread = 0;
	int	count;
	u_char	*tmpbuf;
#ifdef WIN32
	u_char	*tr_buf = NULL;
	int	tr_buf_sz = 0;
#endif


	if (filepath == NULL)
		return (0);


	if (stat(filepath, &sb) == -1) {
		return (0);
	}

	if (sb.st_size <= 0) {
		return (0);
	}

	if ((fd = open(filepath, O_RDONLY)) <= 0) {
		return (0);
	}

#ifdef WIN32
	setmode(fd, O_BINARY);
#endif

	if (sb.st_size > buf_sz) {
		tmp_str = realloc(buf, sb.st_size);
		if (tmp_str == NULL) {
			close(fd);
			return (0);
		}
		buf = tmp_str;
		buf[0] = '\0';
		buf_sz = sb.st_size;
	}

	tmpbuf = buf;
	count = sb.st_size;

	while ( ((nread=read(fd, tmpbuf, count)) > 0) &&
					(nread <= sb.st_size) ) {

		count -= nread;
		tmpbuf += nread;

		if (count == 0) {
			break;
		}
	}

	if (nread < 0) {
		close(fd);
		return (0);
	}

	close(fd);
#ifdef WIN32
	tr_buf = dos2unix(buf, sb.st_size, &tr_buf_sz);
	return (crc(tr_buf, tr_buf_sz));
#else
	return (crc(buf, sb.st_size));
#endif
}

