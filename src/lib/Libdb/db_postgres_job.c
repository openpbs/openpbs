	pj->ji_state = strtol(PQgetvalue(res, row,
		PQfnumber(res, "ji_state")), NULL, 10);
	pj->ji_substate = strtol(PQgetvalue(res, row,
		PQfnumber(res, "ji_substate")), NULL, 10);
