/**
 * @brief
 *	Find a subjob related to a jobid
 *
 * @param[in]	conn - Connection handle
 * @param[in]	st   - The cursor state
 * @param[in]	obj  - Job information is loaded into this object
 * @param[in]	opts - Options (flag & timestamp)
 *
 * @return      Error code
 * @retval	-1 - Failure
 * @retval	 0 - Success
 * @retval	 1 -  Success but no rows found
 *
 */
int
pg_db_find_subjob(pbs_db_conn_t *conn, void *st, pbs_db_obj_info_t *obj,
	pbs_db_query_options_t *opts)
{
	PGresult *res;
	pg_query_state_t *state = (pg_query_state_t *) st;
	pbs_db_subjob_info_t *psubjob = obj->pbs_db_un.pbs_db_subjob;
	int rc;
	int params;

	if (!state)
		return -1;

	LOAD_STR(conn, psubjob->ji_jobid, 0);
	params=1;
	strcpy(conn->conn_sql, STMT_FIND_SUBJOB_ORDBY_INDEX);
	if ((rc = pg_db_query(conn, conn->conn_sql, params, &res)) != 0)
		return rc;

	state->row = 0;
	state->res = res;
	state->count = PQntuples(res);

	return 0;
}

/**
 * @brief
 *	load subjob information into object
 *
 * @param[in]	res  - resultset from an earlier query
 * @param[in]	psubjob  - Subjob object to load into
 * @param[in]	row - The row in the resultset to work on
 *
 */
static void
load_subjob(PGresult *res, pbs_db_subjob_info_t *psubjob, int row)
{
	strcpy(psubjob->ji_jobid, PQgetvalue(res, row,
		PQfnumber(res, "ji_jobid")));
	psubjob->trk_index = strtol(PQgetvalue(res, row,
		PQfnumber(res, "trk_index")), NULL, 10);
	psubjob->trk_status = strtol(PQgetvalue(res, row,
		PQfnumber(res, "trk_status")), NULL, 10);
	psubjob->trk_error = strtol(PQgetvalue(res, row,
		PQfnumber(res, "trk_error")), NULL, 10);
	psubjob->trk_exitstat = strtol(PQgetvalue(res, row,
		PQfnumber(res, "trk_exitstat")), NULL, 10);
	psubjob->trk_substate = strtol(PQgetvalue(res, row,
		PQfnumber(res, "trk_substate")), NULL, 10);
	psubjob->trk_stgout = strtol(PQgetvalue(res, row,
		PQfnumber(res, "trk_stgout")), NULL, 10);
}

/**
 * @brief
 *	Get the next subjob from the cursor
 *
 * @param[in]	conn - Connection handle
 * @param[in]	st   - The cursor state
 * @param[in]	obj  - SubJob information is loaded into this object
 *
 * @return      Error code
 *		(Even though this returns only 0 now, keeping it as int
 *			to support future change to return a failure)
 * @retval	 0 - Success
 *
 */
int
pg_db_next_subjob(pbs_db_conn_t *conn, void *st, pbs_db_obj_info_t *obj)
{
	pg_query_state_t *state = (pg_query_state_t *) st;
	pbs_db_subjob_info_t *psubjob = obj->pbs_db_un.pbs_db_subjob;

	load_subjob(state->res, psubjob, state->row);

	return 0;
}
