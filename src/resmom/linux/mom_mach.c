unsigned linux_time = 0;
/**
 * @brief
 * 	support routine for getting system time -- sets linux_time
 *
 * @return	Void
 *
 */
void
proc_get_btime(void)
{
	FILE *fp;
	char label[256];

	if ((fp = fopen("/proc/stat", "r")) == NULL)
		return;

	while (!feof(fp)) {
		fscanf(fp, "%s", label);
		if (strcmp(label, "btime")) {
			fscanf(fp, "%*[^\n]%*c");
		} else {
			fscanf(fp, "%u", &linux_time);
			fclose(fp);
			return;
		}
	}

	fclose(fp);
	return;
}

