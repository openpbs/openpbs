int
tcl_stat(char *type, struct  batch_status *bs, int f_opt)
{
	struct	batch_status	*bp;
	char	*twol[2];
	char	*argv[ARGNUM];
	int	i, num = 0;
	char	*result;

	if (interp == NULL)
		return 1;

	if (f_opt == 0)
		return 1;

	twol[0] = type;
	for (bp=bs; bp; bp=bp->next) {
		char	*threel[3];

		threel[0] = bp->name;
		threel[1] = attrlist(bp->attribs);
		threel[2] = bp->text;

		argv[num++] = Tcl_Merge(3, threel);
		free(threel[1]);	/* malloc'ed in attrlist() */
		if (num == ARGNUM)
			break;
	}
	twol[1] = Tcl_Merge(num, argv);
	for (i=0; i<num; i++)
		free(argv[i]);

	result = Tcl_Merge(2, twol);
	Tcl_SetVar(interp, "objects", result,
		TCL_GLOBAL_ONLY|
		TCL_LIST_ELEMENT|
		TCL_APPEND_VALUE);
	free(twol[1]);
	free(result);
	return 0;
}
